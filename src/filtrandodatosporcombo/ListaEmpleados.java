    public void cargarComboBoxArea() {
        areasCombo.removeAllItems();
        Vector<ComboArea> oListaObjetoParaComboBoxArea = daoComboArea.ListaDeObjetosParaComboBoxArea();

        for (int i = 0; i < oListaObjetoParaComboBoxArea.size(); i = i + 1) {
            ComboArea oItem = new ComboArea(oListaObjetoParaComboBoxArea.get(i).getId_area(), oListaObjetoParaComboBoxArea.get(i).getDescripcion());

            areasCombo.addItem(oItem);
            
            // areasCombo.addItem(oItem.getId_area()+" "+oItem.getDescripcion());
        }
    }
    

