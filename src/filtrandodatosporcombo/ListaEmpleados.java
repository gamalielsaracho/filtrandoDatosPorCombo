    private void seccionesComboMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_seccionesComboMouseEntered
        // TODO add your handling code here:
        ComboSeccion olistaSecciones = (ComboSeccion) seccionesCombo.getSelectedItem();

        int seccionId = olistaSecciones.getId_seccion();

        System.out.println("EL ID ES---> "+seccionId);
        
        // System.out.println("EL ID ES---> "+areaId);

        DefaultTableModel modelo = (DefaultTableModel)  tablaEmpleado.getModel(); 
        modelo.setRowCount(0);
        
        List<Empleado> empleados = new ArrayList<Empleado>();
        empleados = daoEmpleado.listarEmpleadosByIdSeccion(seccionId);
        
        for (int i = 0; i < empleados.size(); i = i+1)
        {   
            Empleado empl= new Empleado();
            empl=empleados.get(i);
            
            modelo.addRow( new Object[]{
                    empl.getCi(), empl.getNombre(),
                    empl.getApellido() });
       }
    }//GEN-LAST:event_seccionesComboMouseEntered

