    private void areasComboMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_areasComboMouseEntered
        // TODO add your handling code here:
        ComboArea olistaAreas = (ComboArea) areasCombo.getSelectedItem();

        int areaId = olistaAreas.getId_area();

        // System.out.println("EL ID ES---> "+areaId);

        seccionesCombo.removeAllItems();
        Vector<ComboSeccion> oListaObjetoParaComboBoxSeccion = daoComboSeccion.ListaDeObjetosParaComboBoxSeccionByIdArea(areaId);

        for (int i = 0; i < oListaObjetoParaComboBoxSeccion.size(); i = i + 1) {
            ComboSeccion oItem = new ComboSeccion(oListaObjetoParaComboBoxSeccion.get(i).getId_seccion(), oListaObjetoParaComboBoxSeccion.get(i).getDescripcion());

            // seccionesCombo.addItem(oItem.getId_seccion()+" "+oItem.getDescripcion());
            seccionesCombo.addItem(oItem);

        }
    }//GEN-LAST:event_areasComboMouseEntered

